"""
演示Python中变量的相关操作
"""

# 定义一个变量，用来记录钱包余额
money = 50
# 通过print语句，输出变量记录的内容
print("您的余额还有：",money,"元")

# 买了一个冰淇淋，花费10元
money = money - 10
print("买了冰淇淋花费了10元，还剩于：",money,"元")

# 假如每一小时发送一次余额
print("现在是1小时，当前余额：",money,"元")
print("现在是2小时，当前余额：",money,"元")
print("现在是3小时，当前余额：",money,"元")
print("现在是4小时，当前余额：",money,"元")
print("现在是5小时，当前余额：",money,"元")